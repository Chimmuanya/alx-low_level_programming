/**
 * _abs - entry function
 * return absolute value of an integer
 * @n: int to be converted.
 * Return: integer.
 */
int _abs(int n)
{

	n = (int) n;

	if (n > 0)
	{
		return (n);
	}
	else if (n < 0)
	{
		n = n * -1;
		return (n);
	}
	else
	{
		return (0);
	}



}
